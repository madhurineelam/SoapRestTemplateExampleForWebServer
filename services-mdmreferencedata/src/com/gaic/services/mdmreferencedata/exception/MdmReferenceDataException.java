package com.gaic.services.mdmreferencedata.exception;

public class MdmReferenceDataException extends Exception{

	
	private static final long serialVersionUID = 1L;

	public MdmReferenceDataException() {
		super();
	}

	public MdmReferenceDataException(String message) {
		super(message);
	}

	public MdmReferenceDataException(Throwable cause) {
		super(cause);
	}

	public MdmReferenceDataException(String message, Throwable cause) {
		super(message, cause);
	}
}
